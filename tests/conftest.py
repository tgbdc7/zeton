class AuthActions:
    def __init__(self, client):
        self._client = client

    def login(self, username, password):
        return self._client.post(
            "/login", data={"login": username, "password": password}
        )

    def logout(self):
        return self._client.get("/logout")


@pytest.fixture
def auth(client):
    return AuthActions(client)
